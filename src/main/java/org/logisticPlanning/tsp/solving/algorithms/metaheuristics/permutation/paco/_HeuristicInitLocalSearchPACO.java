abstract class _HeuristicInitLocalSearchPACO extends HeuristicInitPACO {
  /** the serial version uid */
  private static final long serialVersionUID = 1L;

  /**
   * the neighborhood manager
   * 
   * @serial a non-null instance
   */
  private UnaryOperator<int[]> m_mutation;

  /** a temporary individual */
  private transient Individual<int[]> m_tempA;

  /** a temporary individual */
  private transient Individual<int[]> m_tempB;

  /**
   * instantiate
   * 
   * @param name
   *          the name
   */
  _HeuristicInitLocalSearchPACO(final String name) {
    super(name);
    this.m_mutation = this.createLocalSearch();
  }

  /**
   * Create the local search operator to be used
   * 
   * @return the local search operator to be used
   */
  protected abstract UnaryOperator<int[]> createLocalSearch();

  /** {@inheritDoc} */
  @Override
  public final _HeuristicInitLocalSearchPACO clone() {
    _HeuristicInitLocalSearchPACO res;
    res = ((_HeuristicInitLocalSearchPACO) (super.clone()));
    res.m_mutation = ((UnaryOperator<int[]>) (res.m_mutation.clone()));
    res.m_tempA = null;
    res.m_tempB = null;
    return res;
  }

  /** {@inheritDoc} */
  @Override
  public void printConfiguration(final PrintStream ps) {
    super.printConfiguration(ps);
    this.m_mutation.printConfiguration(ps);
  }

  /** {@inheritDoc} */
  @Override
  public void printParameters(final PrintStream ps) {
    super.printParameters(ps);
    this.m_mutation.printParameters(ps);
  }

  /** {@inheritDoc} */
  @Override
  public void configure(final Configuration config) {
    super.configure(config);
    this.m_mutation.configure(config);
  }

  /** {@inheritDoc} */
  @Override
  protected long refineSolution(final int[] solution,
      final long tourLength, final ObjectiveFunction f) {
    this.m_tempA.solution = solution;
    this.m_tempA.tourLength = tourLength;
    this.m_tempB.solution = solution;
    this.m_mutation.mutate(this.m_tempB, f, this.m_tempA);
    return this.m_tempB.tourLength;
  }

  /** {@inheritDoc} */
  @Override
  public void beginRun(final ObjectiveFunction f) {
    super.beginRun(f);
    this.m_mutation.beginRun(f);
    this.m_tempA = new Individual<>();
    this.m_tempB = new Individual<>();
  }

  /** {@inheritDoc} */
  @Override
  public void endRun(final ObjectiveFunction f) {
    this.m_tempA = null;
    this.m_tempB = null;
    try {
      this.m_mutation.endRun(f);
    } finally {
      super.endRun(f);
    }
  }
}
